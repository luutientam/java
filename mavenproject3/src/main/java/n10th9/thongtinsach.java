    private void load_tacgia(){
        try{
            tblSach.removeAll();
            Connection con = connectDB.KetnoiDB();
            //tao doi tuong statement de thuc hien lenh truy van
            String sql = "Select * From Products";
            Statement st=con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            String[] tieude = {"Mã Sách", "Tiêu đề","Giá","Số lượng"};
            DefaultTableModel tb = new DefaultTableModel(tieude,0);
            while(rs.next()){
                Vector v = new Vector();
                v.add(rs.getString("ProductCode"));
                v.add(rs.getString("Description"));
                v.add(rs.getString("UnitPrice"));
                v.add(rs.getString("OnHandQuantity"));
                tb.addRow(v);
            }
            tblSach.setModel(tb);
            con.close();
        }catch(Exception ex){
            ex.printStackTrace();
        }
    }

