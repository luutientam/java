    private void tblSachMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSachMouseClicked
        // TODO add your handling code here:
         int i = tblSach.getSelectedRow();  // Get selected row index
    DefaultTableModel tb = (DefaultTableModel) tblSach.getModel();

    if (i >= 0) { // Check if a valid row is selected
        Object maSach = tb.getValueAt(i, 0);
        Object tieuDe = tb.getValueAt(i, 1);
        Object gia = tb.getValueAt(i, 2);
        Object soLuong = tb.getValueAt(i, 3);

        // Check if the cell values are not null before setting them
        if (maSach != null) {
            txtmasach.setText(maSach.toString());
        }

        if (tieuDe != null) {
            txttieude.setText(tieuDe.toString());
        }

        if (gia != null) {
            txtgia.setText(gia.toString());
        }

        if (soLuong != null) {
            txtsoluong.setText(soLuong.toString());
        }

        txtmasach.setEnabled(false); // Disable editing of the book code
    }
    }//GEN-LAST:event_tblSachMouseClicked

