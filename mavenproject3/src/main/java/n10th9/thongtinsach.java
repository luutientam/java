    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
    // Get data from input fields
    String productCode = txtmasach.getText(); // Assuming txtmasach contains the ProductCode
    String description = txttieude.getText();
    String unitPrice = txtgia.getText();
    String onHandQuantity = txtsoluong.getText();

    // Validate input
    if (productCode.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Phải nhập mã sản phẩm!");
        return;
    }
    if (description.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Phải nhập mô tả sản phẩm!");
        return;
    }
    if (unitPrice.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Phải nhập giá sản phẩm!");
        return;
    }
    if (onHandQuantity.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Phải nhập số lượng sản phẩm!");
        return;
    }

    // Connect to the database
    Connection con = connectDB.KetnoiDB();
    
    // Prepare the SQL update statement
    String sql = "UPDATE Products SET Description = ?, UnitPrice = ?, OnHandQuantity = ? WHERE ProductCode = ?";
    
    // Use PreparedStatement to prevent SQL injection
    PreparedStatement pst = con.prepareStatement(sql);
    pst.setString(1, description); // Mô tả sản phẩm
    pst.setBigDecimal(2, new BigDecimal(unitPrice)); // Giá sản phẩm
    pst.setInt(3, Integer.parseInt(onHandQuantity)); // Số lượng sản phẩm
    pst.setString(4, productCode); // Mã sản phẩm

    // Execute the update
    int rowsAffected = pst.executeUpdate();
    
    // Close the connection
    con.close();

    // Check if the update was successful
    if (rowsAffected > 0) {
        JOptionPane.showMessageDialog(this, "Sửa thành công");
        load_tacgia(); // Refresh the table after update
    } else {
        JOptionPane.showMessageDialog(this, "Không tìm thấy sản phẩm với mã này.");
    }
} catch (Exception ex) {
    ex.printStackTrace();
    JOptionPane.showMessageDialog(this, "Có lỗi xảy ra: " + ex.getMessage());
}
    }//GEN-LAST:event_jButton2ActionPerformed

