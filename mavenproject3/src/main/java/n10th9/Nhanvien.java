    private void LoadPhongban() {
        Connection conn = null;
        try {
            conn = connectDB.KetnoiDB();
            String sql = "Select maphongban From phongban";
            PreparedStatement ps = conn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
            while (rs.next()) {
                model.addElement(rs.getString("maphongban")); // Thay đổi tên cột theo cơ sở dữ liệu
            }

            phongban.setModel(model);
            rs.close();
            ps.close();
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
   
    

    private void LoadNhanvien() {
        Connection conn = null;
        try {
            conn = connectDB.KetnoiDB();
            String sql = "Select * from nhanvien";
            PreparedStatement ps = conn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            String[] columnNames = {"Mã nhân viên", "Họ và tên", "Ngày sinh", "Giới tính", "Điện thoại", "Email", "Mã phòng ban", "Ngày vào làm"};
            DefaultTableModel model = new DefaultTableModel(columnNames, 0);

            while (rs.next()) {
                Object[] row = new Object[columnNames.length];
                row[0] = rs.getString("manv");
                row[1] = rs.getString("tennv");
                row[2] = rs.getDate("ngaysinh");
                row[3] = rs.getString("gioitinh");
                row[4] = rs.getString("dienthoai");
                row[5] = rs.getString("email");
                row[6] = rs.getString("maphongban");
                row[7] = rs.getDate("ngayvaolam");
                model.addRow(row);
            }

            // Gán model cho jTable
            nhanvien.setModel(model);

            rs.close();
            ps.close();
            conn.close();
        } catch (SQLException e) {
            System.out.println("Lỗi khi lấy dữ liệu nhân viên");
            e.printStackTrace();
        }
    }

