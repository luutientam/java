    private void nhanvienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_nhanvienMouseClicked
        int row = nhanvien.rowAtPoint(evt.getPoint());
        if (row >= 0) {
            // Lấy thông tin từ hàng được chọn
            String maNV = nhanvien.getValueAt(row, 0).toString(); // Cột mã nhân viên
            String hoTen = nhanvien.getValueAt(row, 1).toString(); // Cột tên nhân viên
            java.util.Date ngaySinh = (java.util.Date) nhanvien.getValueAt(row, 2); // Cột ngày sinh
            String gioiTinh = nhanvien.getValueAt(row, 3).toString(); // Cột giới tính
            String dienThoai = nhanvien.getValueAt(row, 4).toString(); // Cột điện thoại
            String em = nhanvien.getValueAt(row, 5).toString(); // Cột email
            String maPhongBan = nhanvien.getValueAt(row, 6).toString(); // Cột mã phòng ban
            java.util.Date ngayVaoLam = (java.util.Date) nhanvien.getValueAt(row, 7); // Cột ngày vào làm

            // Hiển thị thông tin vào các JTextField
            manv.setText(maNV);
            hovaten.setText(hoTen);
            ngaysinh.setDate(ngaySinh);
            gioitinh.setSelectedItem(gioiTinh);
            dienthoai.setText(dienThoai);
            email.setText(em);
            phongban.setSelectedItem(maPhongBan);
            ngayvaolam.setDate(ngayVaoLam);
        }
    }//GEN-LAST:event_nhanvienMouseClicked

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String maNV = manv.getText();
        String hoTen = hovaten.getText();
        Date ns = new Date(ngaysinh.getDate().getTime());
        String gioiTinh = gioitinh.getSelectedItem().toString();
        String dienThoai = dienthoai.getText();
        String em = email.getText(); // Email
        String maPhongBan = phongban.getSelectedItem().toString(); // Mã phòng ban
        Date nvl = new Date(ngaysinh.getDate().getTime());

        // Kiểm tra xem mã nhân viên có hợp lệ hay không
        if (maNV.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập mã nhân viên.");
            return;
        }

        Connection conn = null;
        try {
            // Kết nối tới cơ sở dữ liệu
            conn = connectDB.KetnoiDB();
            String sql = "UPDATE nhanvien SET tennv = N'" + hoTen + "', ngaysinh = '" + ns + "', gioitinh = N'" + gioiTinh + "', dienthoai = '" + dienThoai + "', email = '" + em + "', maphongban = '" + maPhongBan + "', Ngayvaolam = '" + nvl + "' WHERE manv = '" + maNV + "'";
            Statement st = conn.createStatement();

            // Thực thi câu lệnh
            int rowsUpdated = st.executeUpdate(sql);

            // Kiểm tra kết quả
            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(null, "Cập nhật thành công.");
            } else {
                JOptionPane.showMessageDialog(null, "Không tìm thấy mã nhân viên để cập nhật.");
            }
            LoadNhanvien();
            // Đóng kết nối
            conn.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Lỗi cập nhật dữ liệu: " + ex.getMessage());
        }
    }//GEN-LAST:event_jButton4ActionPerformed

