    private void timkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_timkiemActionPerformed
        String txt = timkiem.getText().trim();
        try {
            String manv = tkmanv.getText().trim();
            String ten = tkhoten.getText().trim();
            Connection conn = connectDB.KetnoiDB();
            Statement st = conn.createStatement();
            String sql = "Select * from nhanvien where manv like '%" + manv + "%' and tennv like N'%" + ten + "%'";
            ResultSet rs = st.executeQuery(sql);
            //   tbLoaiSach.removeAll();
            String[] arr = {"Mã nhân viên", "Họ và tên", "Ngày sinh", "Giới tính", "Điện thoại", "Email", "Mã phòng ban", "Ngày vào làm"};
            DefaultTableModel model = new DefaultTableModel(arr, 0);
            while (rs.next()) {
                Vector vt = new Vector();
                vt.add(rs.getString("manv"));
                vt.add(rs.getString("tennv"));
                vt.add(rs.getString("ngaysinh"));
                vt.add(rs.getString("gioitinh"));
                vt.add(rs.getString("dienthoai"));
                vt.add(rs.getString("email"));
                vt.add(rs.getString("maphongban"));
                vt.add(rs.getString("ngayvaolam"));
                model.addRow(vt);
            }
            nhanvien.setModel(model);
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_timkiemActionPerformed

