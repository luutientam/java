    private void slKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_slKeyReleased
        if(!(evt.getKeyChar()>='0'&evt.getKeyChar()<='9') || evt.getKeyChar()==(char)8 || evt.getKeyChar()=='.'){
//            JOptionPane.showMessageDialog(this, "Phải nhập số");
            tbsl.setText("Phải nhập số !");
            String s = sl.getText();
            sl.setText(s.substring(0,s.length()-1));
        }else{
            tbsl.setText("");
        }
    }//GEN-LAST:event_slKeyReleased

