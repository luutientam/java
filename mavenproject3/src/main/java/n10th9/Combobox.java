    private void load_cbotacgia(){
        try {
            Connection con = connectDB.KetnoiDB();
            // Tao doi tuong statement thuc thi cau lech truy van
            String sql = "Select * from tacgia";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            // truyen du lieu vao combobox
            while(rs.next()){
                comboboxtg.addItem(rs.getString("Hoten"));
                tacgia.put(rs.getString("Hoten"), rs.getString("Matacgia"));
            }
            con.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

