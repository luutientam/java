    private void tacgiaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tacgiaMouseClicked
        int i = tacgia.getSelectedRow();
        DefaultTableModel tb = (DefaultTableModel) tacgia.getModel();
        matacgia.setText(tb.getValueAt(i, 0).toString());
        hovaten.setText(tb.getValueAt(i, 1).toString());
        String ngay = tb.getValueAt(i, 2).toString();
        java.util.Date ns;
        try {
            ns = new SimpleDateFormat("yyyy-MM-dd").parse(ngay);
            ngaysinh.setDate(ns);
        } catch (Exception e) {
            e.printStackTrace();
        }
        chongioitinh.setSelectedItem(tb.getValueAt(i, 3).toString());
        dienthoai.setText(tb.getValueAt(i, 4).toString());
        email.setText(tb.getValueAt(i, 5).toString());
        diachi.setText(tb.getValueAt(i, 6).toString());
        matacgia.setEnabled(false);
    }//GEN-LAST:event_tacgiaMouseClicked

